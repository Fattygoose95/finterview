// 修复缺失的detailedAnalysis字段
const fs = require('fs');
const path = require('path');

// 读取questions.js文件
const content = fs.readFileSync(path.join(__dirname, 'questions.js'), 'utf8');

// 提取questionBank数组
const startMarker = 'const questionBank = [';
const endMarker = '];';

const startIndex = content.indexOf(startMarker);
let endIndex = content.lastIndexOf(endMarker);

if (startIndex === -1 || endIndex === -1) {
    console.error('无法找到questionBank数组');
    process.exit(1);
}

// 提取并解析
const arraySection = content.substring(startIndex + startMarker.length - 1, endIndex + 1);

try {
    // 修复JSON并解析
    let jsonStr = arraySection
        .replace(/,\s*]/g, ']')
        .replace(/,\s*}/g, '}');
    
    const questionBank = eval(`(${jsonStr})`);
    
    console.log(`检查 ${questionBank.length} 个问题的detailedAnalysis`);
    
    let fixedCount = 0;
    const requiredFields = ['overview', 'whyWorks', 'commonMistakes', 'improvementTips'];
    
    // 检查并修复
    for (let i = 0; i < questionBank.length; i++) {
        const q = questionBank[i];
        
        if (!q.detailedAnalysis || typeof q.detailedAnalysis !== 'object') {
            // 创建新的detailedAnalysis
            q.detailedAnalysis = generateMissingAnalysis(q.question || '', q.role || '', q.title || '', q.category || '');
            fixedCount++;
        } else {
            // 检查并补充缺失字段
            const missingFields = requiredFields.filter(f => !q.detailedAnalysis[f] || q.detailedAnalysis[f].length === 0);
            
            if (missingFields.length > 0) {
                // 补充缺失字段
                const newAnalysis = generateMissingAnalysis(q.question || '', q.role || '', q.title || '', q.category || '');
                
                missingFields.forEach(field => {
                    if (!q.detailedAnalysis[field] || q.detailedAnalysis[field].length === 0) {
                        q.detailedAnalysis[field] = newAnalysis[field];
                    }
                });
                
                fixedCount++;
            }
        }
    }
    
    console.log(`修复了 ${fixedCount} 个问题的detailedAnalysis`);
    
    // 保存修复
    if (fixedCount > 0) {
        saveFixedQuestions(questionBank, fixedCount);
    } else {
        console.log('无需修复');
    }
    
} catch (error) {
    console.error('处理错误:', error.message);
}

// 生成缺失的分析
function generateMissingAnalysis(questionText, role, title, category) {
    const questionLower = questionText.toLowerCase();
    let questionType = 'technical';
    
    if (questionLower.includes('behavioral') || questionLower.includes('experience')) {
        questionType = 'behavioral';
    } else if (questionLower.includes('calculate') || questionLower.includes('formula')) {
        questionType = 'quantitative';
    } else if (questionLower.includes('explain') || questionLower.includes('define')) {
        questionType = 'conceptual';
    } else if (questionLower.includes('market') || questionLower.includes('trend')) {
        questionType = 'market';
    }
    
    return {
        overview: `This question assesses ${role} knowledge of ${title || 'key concepts'} through a ${questionType} approach.`,
        whyWorks: `Effective answers demonstrate both theoretical understanding and practical ${role} application, showing how concepts translate to real-world scenarios.`,
        commonMistakes: [
            "Providing generic responses without role-specific context",
            "Failing to structure the answer clearly for easy comprehension",
            "Neglecting to connect concepts to current industry practices",
            "Using excessive jargon without clear explanations"
        ],
        improvementTips: [
            "Reference recent industry examples or case studies",
            "Use a clear framework to organize your thoughts",
            "Balance depth with clarity for different interviewer backgrounds",
            "Connect theoretical concepts to practical job responsibilities"
        ]
    };
}

