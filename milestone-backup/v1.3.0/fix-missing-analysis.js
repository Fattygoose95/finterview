// 更新版本号
function updateVersion() {
    const htmlFiles = [
        'index.html',
        'finance-bro.html', 
        'industry-practice.html',
        'industry-filter.html',
        'profile.html'
    ];
    
    const newVersion = `v${new Date().toISOString().split('T')[0].replace(/-/g, '')}_analysis_fixed`;
    
    htmlFiles.forEach(filename => {
        const filePath = path.join(__dirname, filename);
        if (fs.existsSync(filePath)) {
            let content = fs.readFileSync(filePath, 'utf8');
            content = content.replace(/v=\d{8}_[a-z_]+/g, `v=${newVersion}`);
            fs.writeFileSync(filePath, content, 'utf8');
            console.log(`更新 ${filename} 版本号`);
        }
    });
    
    console.log(`\n✅ 修复完成。所有问题的detailedAnalysis现在都有完整字段。`);
}